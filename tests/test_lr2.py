def get_test_questions_lr2(num_questions: int = 30) -> list:
    """
    Возвращает случайные вопросы из пула в возрастающем порядке индексов
    """
    selected_indices = sorted(random.sample(range(len(QUESTION_POOL)), num_questions))
    questions = []
    for idx in selected_indices:
        q = QUESTION_POOL[idx].copy()
        q['original_index'] = idx
        questions.append(q)
    return questions


def check_answer(question: dict, user_answer: int) -> bool:
    """Проверяет правильность ответа"""
    return user_answer == question['correct']


def calculate_result(correct_count: int, total_count: int = 50, threshold: int = 40) -> dict:  # ✅ Добавьте параметр threshold
    """Вычисляет результат теста"""
    percentage = (correct_count / total_count) * 100
    passed = correct_count >= threshold  # ✅ Используйте параметр threshold
    return {
        "correct": correct_count,
        "total": total_count,
        "percentage": percentage,
        "passed": passed,
        "threshold": threshold  # ✅ Возвращайте переданный порог
    }